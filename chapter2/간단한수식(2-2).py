# 2-2 간단한 수식

print(2 + 3 * 4) # 14
print((2 + 3) * 4) # 20

number = 2 + 3 * 4 # 14 (변수 사용)
print(number)

number = number + 2 # 16
print(number)

number += 2 # 9번 문장과 같은 의미
print(number) # 18

number /= 2 # 9
print(number)

number *= 2 # 18
print(number)

number -= 2 # 16
print(number)

number %= 5 # 1
print(number)